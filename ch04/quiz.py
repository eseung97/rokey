# 감정별 추천 음식을 저장하는 딕셔너리
food_recommend = {
    "기쁨": ["🍦 아이스크림", "🍫 초콜릿", "🥩스테이크"],
    "슬픔": ["🍜 라면", "🌶️ 떡볶이", "🍲 마라탕"],
    "피곤함": ["🍗 삼계탕", "🍊 비타민 주스", "🍵 해장국"]
}

# 사용자의 감정을 입력받기
mood = input("오늘 기분이 어때요? (기쁨, 슬픔, 피곤함 중 하나 입력) : ")

# 여기에 조건문을 작성해서 해당 감정에 맞는 음식을 추천해 보세요!
if mood == "기쁨":
    print(food_recommend["기쁨"])
elif mood == "슬픔":
    print(food_recommend["슬픔"])
else:
    print(food_recommend["피곤함"])
